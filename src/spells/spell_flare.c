#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <time.h>
#include "globals.h"
#include "tables.h"
#include "magic.h"

bool spell_flare(int sn, int level, CHAR_DATA *ch, void *vo, OBJ_DATA *obj)
{
   CHAR_DATA *victim = (CHAR_DATA *)vo;
   AFFECT_DATA af;

   if (IS_AFFECTED(victim, AFF_BLIND) || saves_spell(level, victim))
      return TRUE;

   if (victim == ch)
      return FALSE;

   af.type = sn;
   af.location = APPLY_HITROLL;
   af.modifier = -10 - (level / 4);
   af.duration = 20;
   af.duration_type = DURATION_ROUND;
   af.bitvector = AFF_BLIND;
   affect_to_char(victim, &af);
   act("$n invokes the power of Ra to produce a solar flare which blinds $N!", ch, NULL, victim,
       TO_NOTVICT);
   act("$N invokes the power of Ra to produce a solar flare.  You are BLINDED!", victim, NULL, ch,
       TO_CHAR);
   act("You invoke the power of Ra to produce a solar flare, which blinds $N!", ch, NULL, victim,
       TO_CHAR);

   return TRUE;
}
