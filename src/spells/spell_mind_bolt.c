#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <time.h>
#include "globals.h"
#include "tables.h"
#include "magic.h"

bool spell_mind_bolt(int sn, int level, CHAR_DATA *ch, void *vo, OBJ_DATA *obj)
{
   CHAR_DATA *victim = (CHAR_DATA *)vo;
   int cnt;
   int foo;
   int dam = 0;

   cnt = (level >= 12) + (level >= 30) + (level >= 60) + (ch->level >= 75);
   for (foo = 0; foo < cnt; foo++)
   {
      if (number_percent() < 30)
         continue;

      dam += number_range(12, 30) + (ch->class_level[CLASS_PSI] * 5 / 4);
   }

   if (dam <= 0)
      return TRUE;

   if (saves_spell(level, victim))
      dam /= 2;

   sp_damage(obj, ch, victim, dam, ELEMENT_MENTAL, sn, TRUE);
   return TRUE;
}
